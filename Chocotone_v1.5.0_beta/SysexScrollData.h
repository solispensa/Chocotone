// Message length for PITCH_HIGH list (all messages are 41 bytes)
#define PITCH_HIGH_MSG_LEN 41

// Get SysEx scroll list by param ID
inline const SysexScrollList *getSysexScrollList(SysexScrollParamId id) {
  static const SysexScrollList PITCH_HIGH_LIST = {
      SYSEX_PARAM_PITCH_HIGH, PITCH_HIGH_LIST_SIZE, PITCH_HIGH_MSG_LEN,
      PITCH_HIGH_DATA};

  switch (id) {
  case SYSEX_PARAM_PITCH_HIGH:
    return &PITCH_HIGH_LIST;
  default:
    return nullptr;
  }
}

// Get a specific SysEx message from a list
// Returns pointer to message data in PROGMEM, or nullptr if out of range
inline const uint8_t *getSysexScrollMessage(SysexScrollParamId paramId,
                                            uint8_t msgIndex,
                                            uint8_t *outLength) {
  const SysexScrollList *list = getSysexScrollList(paramId);
  if (!list || msgIndex >= list->msgCount) {
    *outLength = 0;
    return nullptr;
  }

  *outLength = list->msgLength;
  return list->data + (msgIndex * list->msgLength);
}

