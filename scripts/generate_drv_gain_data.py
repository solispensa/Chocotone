def hex_to_c_array(hex_str):
    """Convert hex string to C array bytes"""
    # Remove any whitespace
    hex_str = hex_str.replace(" ", "")
    # Convert pairs of hex chars to bytes
    bytes_list = [f"0x{hex_str[i:i+2]}" for i in range(0, len(hex_str), 2)]
    return bytes_list

